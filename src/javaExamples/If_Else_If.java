package javaExamples;

public class If_Else_If {
public static void main(String[] args) {
	int a=92;
	if (a==80) {
		System.out.println("A garde");
	}
	else if (a==92) {
		System.out.println("B Grade");
	}
	else if (a==60) {
		System.out.println("C Grade");
	}
	else {
		System.out.println("D garde");
	}
}
}
